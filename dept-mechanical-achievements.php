      <h1 class="page-title">Achievements</h1>
      <p>
        <strong>TOP ACHIEVEMENTS OF THE DEPARTMENT</strong><br>

        <strong>ANNA UNIVERSITY RESEARCH CENTER</strong><br>
        The department is approved as a RESEARCH CENTER by ANNA UNIVERSITY for doing M.S and Doctoral programs.<br><br>

        <strong>PERMANENT AFFILIATION</strong><br>
        The Department is PERMANENTLY AFFILIATED by the ANNA UNIVERSITY due to the excellent facilities.<br><br>

        <strong>FELLOWSHIPS and AWARDS</strong><br>
        Dr.Natteri.M.Sudharsan, Professor, Department of mechanical engineering has been selected as Fellow of ASME for his outstanding achievements. He is one among the 3000 fellows all over the world.<br><br>
        Dr.SP.Srinivasan and Mr.C.Devanathan were honoured with Platinum award for Kaizen Competition for Support Functions ABK - Aots Dosokai on 24th September 2016.<br><br>
        Dr. SP. Srinivasan received Fellowship from the Institute of Engineers on 5th January 2017.<br><br>
        Dr. SP. Srinivasan became the Chairman of the Indian Institution of Industrial Engineering during 3rd July 2017.<br><br>

        <strong>DST SPONSORED RESEARCH</strong><br>
        The department has a privilege of getting a DST project worth of 18 lacs to set up a lab to develop and test tyre pyrolysis oil. It aims at developing an alternative method to extract oil from waste tyres by pyrolysis process and testing the oil for its feasibility as a potential fuel for automobiles.<br><br>

        <strong>CONSULTANCY</strong><br>
        The department is actively involved in various consultancy projects like Namakkal Transport carriers, Saint Gobain Glass, MTAB industries and TTK Healthcare. A total amount of more than 5 lacs was generated from the consultancy work.<br><br>

        <strong>PLACEMENT</strong><br>
        The students from the Department of Mechanical Engineering got placed in various core companies such as Ashok Leyland, L&T, KONE elevators, Schwing sttetter, Hyundai, DELL and also in IT companies such as TCS, Wipro. More than 80% of students get placed in various companies every year.<br><br>

        <strong>REC-BOSCH</strong><br>
        A training lab for automotive testing is jointly set up in association with Bosch to train the students in automotive components. The students are exposed to the recent trends in automotive sector.<br><br>

        <strong>MODROBS</strong><br>
        MODROBS is a scheme under the AICTE sponsorship which aims at modernizing the lab and removal obsolescence. Under this scheme, the FMS lab was equipped with state-of-the-art ASRS, AGV and Machine vision system. The Thermal lab is equipped with CVTR to conduct experiments on IC Engine.<br><br>

        <strong>ASOP</strong><br>
        Automotive Student Orientation Programme is a 30 days residential programme conducted for the students. This programme aims at developing Industry specific skills for the students. The students are trained by experts from automotive industries such as WABCO-TVS & SAE INDIA. Experts from SAE southern division conduct classes in this course.<br><br>

        <strong>MSME TRAINING PROGRAMME</strong><br>
        The Department of mechanical engineering conducts Skill Enhancement training programme for the workers ( Women Employees ) of MANDO Brakes, Irungattukottai, Chennai. This program aims at providing the fundamental skills of Mechanical engineering to the employees with other than mechanical specialization. The Ministry of Micro, Small and Medium Enterprises (MSME) provides the certificates for the workers after successful completion of the 2 year course.<br><br>

        <strong>CATIA AND ANSYS TRAINING</strong><br>
        Every academic year the students were provided a certification training program by the industry experts in CATIA and ANSYS. People from EDS Technologies, Bangalore are giving training in CATIA. Ansys training is given by ProCAE solutions, Chennai.<br><br>

        <strong>SABBATICAL TRAINING</strong><br>
        Faculty member from the department have undergone sabbatical training at TVS-Lucas to update and exposed to real time Industrial knowledge.<br><br>

        <strong>MACHINE VISION</strong><br>
        The department faculty members are actively involved in the project in Machine Vision under TIFAC-CORE<br><br>

        <strong>PROFESSIONAL SOCIETY ACTIVITIES</strong><br>
        Various activities are organized through students chapters of various live professional society bodies like ASME (American Society of Mechanical Engineering), NIQR (National Institute for Quality and Reliability), SAE (Society of Automotive Engineers), ISHRAE (Indian society of heating Refrigeration & Air conditioning Engineers) and IIW (Indian Institute of Welding).<br><br>

        <strong>FACULTY PUBLICATIONS</strong><br>
        The faculty members of Mechanical Engineering have published more than 100 papers in various International Journals, National Journals, International and National Conferences. More than 50 papers published in High Impact factor journals.<br><br>

        <strong>INGENIOUS (Clever original and Inventive)</strong><br>
        INGENIOUS is a National Level Design Contest conducted by the Department every year. A total of 1047 students participated in the Ingenious'14. This event is mainly aimed at showcasing and sharing the young talent. Students from various institutes from different part of the country including from Mumbai and kerala attended last year’s event.<br><br>

        <strong>FACULTY DEVELOPMENT PROGRAMMES</strong><br>
        A FDP on Kinematics of Machinery organised from 13-06-2016 to 20-06-2016 in which more than 50 participants attended.<br><br>
        A FDP on Robotics and Industrial Automation was organised on 19-11-2016 in which more than 50 participants attended.<br><br>
        A FDP on Finite Element Analysis organised on 22-12-2016 to 24-12-2016 in which more than 50 participants attended.<br><br>
        A FDP on Finite Element Analysis organised on 14-12-2017 to 16-12-2017 in which more than 50 participants attended.<br><br>
        A FDP on Engineering Thermodynamics was organised from 10-06-2019 to 12-06-2019 in which more than 50 participants attended.<br><br>

        <strong>CONFERENCES</strong><br>
        Conference gives the opportunity to learn and share the latest research findings by the researchers. We conduct paper presentation by the academia every year as part of Ingenious. Also the department conducted International conferences on Recent Advances in Mechanical Engineering, Optimization techniques in Engineering and SERIA'13.<br><br>

        <strong>INGENIOUS (Clever original and Inventive)</strong><br>
        INGENIOUS is a National Level Design Contest conducted by the Department every year. It is conducted in 2016 ,2017 and 2019 in a successful manner. A 1000+ students participated actively. This event is mainly aimed at showcasing and sharing the young talent. Students from various institutes from different part of the country including from Mumbai and Kerala attended in this events.<br><br>

        <strong>RAMS, April 2017</strong><br>
        A National Conference on Recent Advances in Mechanical Science (RAMS 2017) was conducted in our Rajalakshmi Engieering College.The conference aims to provide a platform to discuss the latest trends and ongoing research and share/disseminate the knowledge, thereby popularizing the subject amongst budding engineers and scientist.<br><br>

        <strong>GMAAPPES’ 18</strong><br>
        The Department of Mechanical sciences organized two days international conference on Green manufacturing: Analysis, Automation, Processes, Products, Energy and structures (GMAAPPES’ 18) on 28 th and 29 th march 2019. In the conference totally 150 papers were presented in two days, in 15 different sessions. The conference was inaugurated by Dr.Tony. A. Maciejerrski, Professor / Head Colorado state, USA and Dr.Pramod vatsa, National head, Operation, Sainr gobain (p) Ltd. During valedictory ceremony the certificate was distributed by Mr.Micael J. Gobator- Vice consulate General of Russian federation Chennai, and Dr.L.Vijayaraghavan – Professor IIT Madras. After conference around 18 papers these published as special issue in Journal for advanced research in applied sciences ISSN no: 2394-8442.<br><br>

        <strong>NCRAMS, April 2019</strong><br>
        NCRAMS 19, a two days National Conference on Recent Advances in Mechanical Sciences, one of the important events conducted by the Department of Mechanical Engineering, REC during 15 th and 16 th March, 2019. We received a wide response from various institutions and several departments of REC. Guests from industry and IIT Madras had graced the occasion by their presence. Dr. K. Srinivas Reddy from IIT Madras highlighted the research activities in IIT Madras. Dr. A.J. Jegadheesan from Lucas-TVS enlightened us with latest developments in the industry like Industry 4.0. Dr. A. Ragothaman gave an overview of R&D activities for Trucks in Ashok Leyland. Mr. B. Sankaranarayanan from Sterling and Wilson shared his experiences in the Chennai Metro Ventilation Project. 124 papers were received and 100 shortlisted papers were presented in 7 different sessions. There were some interesting and innovative presentations like automobile accident tracking system, antilock braking system, fuel cell technologies, etc. Learning is a continuous process. Hope NCRAMS provided an opportunity to learn new concepts and ideas from other participants.<br><br>

        <strong>STTP</strong><br>
        AICTE Sponsored Virtual STTP (Phase-IV) on "Composite Materials: Micro to Nano- Fabrication, Characterization and Modelling Including Additive Manufacturing", during 13.07.2020 to 18.07.2020.<br /><br />
        AICTE Sponsored Virtual STTP (Phase-IV) on "Composite Materials: Micro to Nano- Fabrication, Characterization and Modelling Including Additive Manufacturing", during 27.07.2020 to 01.08.2020.<br /><br />
        AICTE Sponsored Virtual STTP (Phase-IV) on "Composite Materials: Micro to Nano- Fabrication, Characterization and Modelling Including Additive Manufacturing", during 31.08.2020 to 05.09.2020.<br /><br />
        AICTE Sponsored Virtual STTP (Phase-IV) on "Composite Materials: Micro to Nano- Fabrication, Characterization and Modelling Including Additive Manufacturing", during 23rd November 2020 to 30th November 2020.<br /><br />

        <strong>TQM Cell</strong><br>
        The department practices TQM concepts such as Kaizen for continuous improvement and 5S for workspace organization. Various activities have been initiated under this cell and successfully implemented many industrial practices in the laboratories. This also makes the students to get exposed to industrial practices.<br><br>
