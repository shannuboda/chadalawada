        <h1 class="page-title">Centre of Excellence in Electric Vehicle Technology (COE-EVT)</h1>
        <p>
          <strong>Vision : </strong>
          <br>
          To be a globally recognised centre of excellence in the field of Electric Vehicle Technology providing/imparting quality education, interdisciplinary research and innovative development of Electric Vehicle hnology.
          <br>
          <br>
          <strong>Mission : </strong>
        <ul>
          <li>To promote scientific discovery and the invention of Electric Vehicles and Battery Technologies</li>
          <li>To undertake cutting edge research and education in the areas of Design of Drive train, Controller Modules and Advancement of Battery manufacturing</li>
          <li>To carry out Interdisciplinary research and education to nurture future experts in Electric Vehicle research by imparting interdisciplinary knowledge, innovative ideas, critical thinking and practical skills.</li>
          <li>To build the capacity of technical personnel fostering strong academic, industrial, and social ties for solving various Technical problems to make an impact on Automobile industry.</li>
          <li>To provide consultancy, training, and outreach services for stakeholders in the region including social institutions.</li>
        </ul>
        <br>
        <strong>History of COE-EVT in REC : </strong>
        <br>
        This Centre of Excellence in Electric Vehicle is the long term dream of the department of Electrical. We concentrated many students project in the field of Electric Vehicle in the year 2012. This kind of initiation gives the road map to Electric Vehicle Lab in our department. Then the association of many faculties of our department and funded project and relationship with industrial and technical persons we valued now with this outcome. This COE- EVT is started in the mid of 2020-2021. Now we are supporting our stake holders in the form of knowledge sharing and initiating research possibilities.
        <br>
        <br>
        <strong>Research Activities: </strong>
        <br>
        To carry out advanced interdisciplinary research activities in the area of Electric Vehicle Technology cater to the needs of the industry and society (in general) and using this as a general research facility to take up new research projects from industry and other bodies.
        <br>
        <ul>
          <li>Conducting training programmes in the related areas for professional skill development.</li>
          <li>Generating Intellectual Properties (IP) in terms of patents and high quality technical publications.</li>
          <li>Conducting consultancy works for industries covering, but not limited to, transportation, manufacturing, agriculture etc.</li>
          <li>Conducting courses with significant participation of external faculty, industrial and regulatory experts on EVT.</li>
          <li>Entering into collaborative research engagements with Extra-mural funding agencies and Industrial Organizations for better understanding of issues of scientific and technological management between different stakeholders.</li>
          <li>Currently Mr. S. B. Ron Carter, Assistant Professor, REC is working for his PhD under the support of COE-EVT</li>
        </ul>
        <br>
