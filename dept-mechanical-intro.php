      <strong>VISION  and MISSION of  the Department(UG &PG)</strong><br>
      <p>
        The Department of Mechanical Engineering, which was started in 1997, provides both undergraduate and post graduate studies. Anna University has recognized it as a research centre for its collaborative research programme that leads to a Ph.D. by research. The NBA has re-accredited the department. It has well-trained faculty with a mix of industrial and academic experience. The department is actively involved in research and consultancy work funded by a variety of funding sources, including DST, UGC, AICTE, GTRE, ISHRAE and others. Faculty and students are actively involved in patent publication. The department's faculty members hold doctorates in a variety of fields and are also qualified for research guidance from Anna University. Around 20 scholars have received their Ph.D from the mechanical research centre, and more than 30 are now pursuing their studies. The faculty and students are also actively engaged in innovative projects, presenting papers in conferences, publishing journal papers and patents.
      </p>

