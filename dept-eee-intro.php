        <p>
          The Department of Electrical and Electronics Engineering was established in the year 1999-2000. Currently, the department offers Undergraduate degree in Electrical and Electronics Engineering and a Postgraduate degree in Embedded Systems. It has also been recognized as a Research Centre by Anna University, Chennai, for offering Ph.D. and M.S (by Research) programme. A number of candidates have enrolled for Ph.D. and are carrying out their research in core areas of Power Electronics and Drives, Power Systems, Special Machines, Renewable Energy and Embedded Systems. The department was reaccredited by the National Board of Accreditation (NBA) , New Delhi, in the year 2016 and was further reaccredited till the year 2022. The department has an elite group of faculty members who are dedicated and well experienced in different fields of specialization. The department is fully equipped with latest systems and apparatus in the Electrical machines, Power Electronics, Internet of Things Applications in Electrical Engineering, Control Systems, Power System Simulation, Embedded Systems, Measurements and Instrumentation laboratories. The mission of the department is to produce highly competent Electrical and Electronics Engineers with a sound knowledge in all the areas concerning the discipline.<br /><br />
