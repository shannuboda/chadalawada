  <h1 class="page-title">Opportunities</h1>
      <p>
        The Electrical & Electronics Engineering degree program prepares the graduates to enter a dynamic and rapidly changing field with career opportunities in Electric Power, Power Electronics, Microprocessors and Controllers, Integrated Circuits, Robotics and Control, Computer Hardware and Software. The demand for electrical power, electronic systems and electric vehicles is increasing rapidly, which has increased the job opportunities for Electrical & Electronics engineers in growing industries such as, Renewable Power Generation, Manufacturing Electrical Equipment, Electronic Systems, Accessories, Research and Development departments which work on energy saving devices and hardware development.
        <br /><br />
        Electrical and Electronics Engineering is arguably the technology which has most shaped the world we live in. All aspects of our lives are affected by the electrical power we take for granted. Electrical and Electronics Engineers are responsible for the continued safe and responsible development of this technology in all its applications, through research, design and development, manufacture, service, marketing and consulting. Electrical and Electronics Engineering is a dynamic and exciting discipline which will continue to serve the needs of society through the economic and responsible exploitation of existing technology and the development of innovative ideas. To meet the needs and challenges, the department in REC has a firm commitment to develop and produce stalwart electrical engineers of high technical knowledge combined with leadership skills, decision making, design capabilities and problem solving.
