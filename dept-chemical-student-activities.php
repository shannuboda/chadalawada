        <h3>Professional Society</h3>
        <h4>IICHE Student Chapter</h4>
        <p>
          Indian Institute of Chemical Engineers (IICHE) is the apex professional body of chemical engineering professionals in India. Student members pursuing under-graduate degrees in Chemical Engineering form Student Chapters, under the auspices of the respective Regional Centres. The Student Chapters arrange lectures, seminars, short courses, plant visits, etc., at regular intervals to better equip and empower the students when they come out of their academic precincts. Academic activities apart, Student Chapters organize cultural events and sports activities for their members.<br />
          Department of Chemical Engineering, Rajalakshmi Engineering College has formed the IICHE Student Chapter. The inauguration of IICHE Student Chapter was held on 6th September 2019 at Rajalakshmi Engineering College. Dr.Ragunathan Rengaswamy, Institute Char Professor, IIT Madras, Chief Guest of the function inaugurated the student chapter. Dr.N.Balasubramanian, VC-IICHE, Chennai , Professor at A.C.Tech and Mr.D.Gokul, Honorary Secretary-IICHE Chennai, Scientist ISRO were the guests of Honour.
