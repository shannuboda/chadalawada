<h2 class="page-title">Distinguished Alumnus Awards</h2>
Dear Alumnus, <br><br>
As you all are aware, Rajalakshmi Engineering College (REC) is ONE of the top institutes of academic excellence in Tamil Nadu. Students graduating from REC are of very high caliber, who have reached positions of eminence in industry, business, public sector, academic and research institutions and many also launched successful entrepreneurial ventures. The institute had long felt the need to recognize the Alumni of REC who have distinguished themselves in their field of work and made the institute proud and enhance its reputation in the academic surface. We are glad to inform you that with this intention, our college has instituted the Distinguished Alumnus Awards.<br><br>
REC is honouring the Distinguished Alumnus with Annual Awards from 2018 onwards. The Office of the Alumni Relations will receive nominations for such Awards and the selection will be made by a committee appointed by the Principal.
Nominations are now being accepted for Distinguished Alumnus Awards for the year 2019. The last date for receiving nominations at the college is 1st December 2019.<br><br>
Nominations may be submitted in the following one or more of the four categories:<br>
1. ENTREPRENEURIAL VENTURES<br>
2. INDUSTRY EMPLOYMENT<br>
3. TEACHING PROFESSION<br>
4. RESEARCHER / SCIENTIST<br><br>
Detailed questionnaire for each of the category is available in the online application.<br><br>
Nomination is a simple online process and is expected to take only a few minutes of time by clicking on the link below. It involves providing some basic information and uploading a CV of the nominee that highlights all her/his achievements that may make her/him a deserving recipient of the award. The person nominating must, therefore, first prepare this document for uploading that amply showcases the achievements of the alumnus with relevant detail before starting the nomination process by clicking on the link.<br><br>
Please upload your photograph and supporting documents for this Distinguished Alumnus Awards. <br><br>
<h3 class="page-title">Selection Process</h3>
1. Office of Alumni Relations receives application till 1st December 2019.<br>
2. Applications are scrutinized and summarized by the Selection Committee appointed by the Principal.<br>
3. Applications are ranked in each category.<br>
4. Category-wise rankings are used to identify the first awardee in each category.<br><br>

