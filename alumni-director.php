<strong>3. Volunteer</strong><br>Step up to help organize an event or contribute your time for a specific cause or project. As part of the workforce, offer to take REC students as interns or even a job if they meet your criteria. This will certainly help our students get real-world experience and find employment, respectively.<br><br>
