      <strong>FDTP Organized by the Department</strong><br>
      The Department of Civil Engineering in association with Anna University, Chennai organized a Six-Day Faculty Development Training Programme on CE6702 - PRESTRESSED CONCRETE STRUCTURES

      from 3rd June 2019 to 10th June 2019. Partial Financial Assistance was provided by Anna University to conduct the programme. Speakers from reputed institutions and industries, shared

      their experience and enlightened the Faculty gathering. Eminent speakers from IIT-Madras, Anna University, B.S. Abdur Rahman Crescent Institute of Science and Technology and Industries

      like Jayam Construction, UtraCon Structural system, AECOM Constructions, Hi-tech Concrete solutions were invited to deliver valuable lecture on prestressed concrete construction. 13

      external participants from various affiliated institutions and 5 internal participants from Rajalakshmi Engineering College attended the training programme and found it to be very

      useful. The programme was inaugurated by Mr. M. Ramanathan, Director-Structures, Transit &amp; Railways, Transportation, AECOM, Chennai followed by his lecture on &ldquo;Basic concepts,

      advantages, systems and methods of prestressing&rdquo;.<br><br>
