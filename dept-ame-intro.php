   <p>
 
The Department of Automobile Engineering was started in the academic year 2006-07 with a sanctioned intake of 60 students. The department has dedicated and qualified faculty members. The department follows the syllabus of the Anna University and to supplement the syllabus, guest lectures and Industrial visits are organized. Laboratories are well equipped with latest equipments such as modern engines, chassis dynamometer, exhaust gas analyzer, pressure crank angle measurement systems, fuel testing equipments, etc.
<br><br>
