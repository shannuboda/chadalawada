                        <h1 class="page-title"> SCOPE OF THE DEPARTMENT</h1>
                        <!-- <div class="page-img"><span class="page-img"><img src="image/dept/chemical-engineering.jpg" alt="Chemical Engineering"></span></div> -->
                        <p>
                            Chemical engineers find work not just in chemicals industry, but also in manufacturing,
                            pharmaceuticals, oil and gas sectors, healthcare, design and construction, pulp and paper,
                            petrochemicals, food processing, specialty chemicals, microelectronics, electronics,
                            advanced materials, polymers, business services, biotechnology, and environmental health and
                            safety industries, among others. Engineering, Procurement and Construction (EPC) companies,
                            which are contractors who complete projects from start to finish for their clients,
                            routinely hire chemical engineers for design related jobs such as designing petrochemical,
                            industrial plants, refineries etc using various software tools. Chemical Engineers sometimes
                            are called "Universal engineers" in view of the broad spectrum of areas in which they
                            contribute. Chemical Engineers also develop solutions to environmental problems, such as
                            pollution control and remediation. And yes, they process chemicals, which are used to make
                            or improve just about everything you see around you.
                        </p>
