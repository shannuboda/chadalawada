                        <p><strong>Chem Skill Development Program</strong></p>
                        <p>In order to develop industry-oriented skills and equip our chemical engineering students with
                            unique methodologies utilized in industry, the department of chemical engineering provides a
                            5-week industry-oriented knowledge building program in collaboration with Chem skill
                            development centre. Through industry-oriented programs, students gain insights into industry
                            standards and best practices, enhancing their competitiveness. By nurturing skills and
                            global perspectives, CSDC aims to develop students into world-class engineers equipped for
                            the global stage. It provides a platform to learn variety of topics such as Chemical Process
                            Design, Unit Operations, Chemical Reaction Engineering, Safety Management and Measures,
                            Environmental Regulations and Compliance, Quality Control and Root Cause Analysis.
                            Participants benefit from theoretical classes as well as 14-day In-Plant training in a
                            Chemical Industry, providing invaluable hands-on experience and a competitive edge in the
                            job market. By fostering collaboration between academia and industry, our department and
                            CSDC continues to shape generation of competent and socially responsible engineers poised to
                            make a meaningful impact on the world stage.

