                        <p>
                            Chemical engineering is a branch of engineering that applies Physical Sciences (Physics and
                            Chemistry), Life sciences (Microbiology and Biochemistry), together with applied mathematics
                            and economics to produce, transform, transport, and properly use chemicals, materials and
                            energy. Chemical Engineering is a blend of traditional Chemical Engineering, Biomedical
                            Engineering, Nanotechnology, Bimolecular Engineering, Pharmaceutical processing, Molecular
                            dynamic simulations of polymeric systems. A chemical engineer designs large-scale processes
                            that convert chemicals, raw materials, living cells, microorganisms and energy into useful
                            forms and products. The course is designed in a way for the students to undergo an extensive
                            course work and extensive laboratory practice in all conventional and advanced areas of
                            Chemical Engineering.
                        </p>
                        <p>
                            <strong>Vision</strong><br>
                            To be a center of excellence in chemical engineering to provide well prepared professionals
                            to the industries and society.<br><br>
                            <strong>Mission</strong><br>
                            i. To provide state of art environment to the students for better learning to cater for the
                            chemical industries and pursue higher studies.<br>
                            ii. To provide space to the students in research to think, create and innovate things.
                        </p>
