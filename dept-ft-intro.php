<p>Department of Food Technology is introduced from the Academic Year 2018-19. <br><br>
The Indian food industry is poised for huge growth increasing its contribution to world food trade every year. It has emerged as the highest growth and profit section due to its immense potential in food processing industry. Global giants are investing billions of dollars in India in this sector. Indian government also aims to set up new processing units, with a strong focus on supply chain infrastructure.<br><br>
The curriculum of B.Tech Food Technology offered in REC provides appropriate importance to all the areas in food science and technology such as Food processing, Preservation, Food safety, Quality control, Equipment design, process Engineering, Innovative and instant food products. It also offers Baking and confectionery technology, fruit and vegetable processing technology, Dairy processing, food packaging, Functional foods and Neutraceuticals, Plantation technology, Refrigeration and cold chain management and Industry training. etc.<br><br>
B.Tech (Food Tech) being a Choice based credit system course offered by an autonomous institution, the ambience is best suited for active learning with industry training, online courses, open electives, facility to go abroad for internships etc and the state of the art labs provide ample opportunity to innovate & research. <br><br>
A team of dedicated, well qualified and experienced teachers teach, guide and motivate the students to excel and come out with flying colors. A student graduating after completion of this course has opportunities in industries, Multinational companies, Hospitality outlets and also as entrepreneurs. They also have the privilege to go abroad for higher studies, management and research. Of course REC is definitely the destination for aspiring engineers.

