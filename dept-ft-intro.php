<div id="lab-facility" class="main-content" style="display: none;">
  <!-- Content for Lab Facility -->
  <h1 class="page-title">Lab Facility</h1><p> 
	<p>Department of Food Technology is having state of the art lab facilities for effective testing of food samples to conform to legal standards. All the labs in the department are exclusively available for quality evaluation of foods with advanced instruments. Some of the Equipment available in Department of Food Technology include UV Spectrophotometer, Gerber Centrifuge, Brookfield Viscometer, Pin mill, Burr mill, Hammer mill, Rotary flash evaporator, Laminar air flow, Fermentation cabinet, Tray dryer, Plate heat exchanger, Pasteurise, Soxhlet and Kjeldhal apparatus etc., </p>
	<br>
	<h4 class="page-title">Food Chemistry Laboratory</h4> 
		<div class="page-img"><img src="image/dept/lab1.jpg" alt="Food Technology" /></div>

	<p>Students are able to recognize the important reactions in food chemistry and get familiar with methods to measure chemical reactions involved in foods. They also understand a simple food chemistry problem practically by handling various instruments</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Digital Calorimeter, Cyclomixer, pH Meter, Digital Weighing Balance, Water Bath, UV- Visible Spectrophotometer, Kjeldahl Distillation Unit, Stature Meter, Pycnometer fitted with thermometer Brookfield Refractometer </p>
	<br>
	<h4 class="page-title">Food Processing and Preservation Laboratory</h4> 	
	<div class="page-img"><img src="image/dept/ftlab2.jpg" alt="Food Technology" /></div>

	<p>Students are able to understand the major unit operations involved in Food preservation & processing and explore the applications of different novel processing techniques using advanced instruments.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Pasteurizer, Vegetable Chopper, Mixer, Plate Heat Exchanger, Deep Freezer, Tray Dryer, Hot Air Oven, Hand Refractometer, Extruder, Retort Unit, Water Vapour Transmission Tester, Viscometer</p>
	<br>
	<h4 class="page-title">Chemical Engineering Laboratory</h4> 
		<div class="page-img"><img src="image/dept/ftlab3.jpg" alt="Food Technology" /></div>

	<p>Students will learn basic operations, flow measurements and calculate the efficiencies of equipment involved in the sequence of Food Processing operations.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Rotary Evaporator, Orifice Meter, Venturi Meter, Rotameter, Packed Column, Centrifugal Separator, Steam Distillation Unit, Fluidized Bed Column, Rotary Flash Evaporator, Cyclone Separator, Ball Mill, Hammer Mill, Bur Mill </p>
	<br>
	<h4 class="page-title">Biochemistry and Nutrition Laboratory</h4> 
		<div class="page-img"><img src="image/dept/ftlab4.jpg" alt="Food Technology" /></div>

	<p>Students will be able to learn fundamental measurements, chemical analysis involved in Food Biochemistry along with, nutritional anthropometry and clinical status of the individuals </p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Digital Calorimeter, Muffle Furnace, Viscometer, Vortex Mixer, Pycnometer, Refractometer, Soxhlet Apparatus, Heating Mantles, Microscope, Hot Air Oven, Analytical Balance, Moisture Balance Stadiometer, Oil Bath, Digital Weighing Balance, Kjeldahl Distillation Unit</p>
	<br>
	<h4 class="page-title">Food Microbiology Laboratory</h4> 
		<div class="page-img"><img src="image/dept/ftlab5.jpg" alt="Food Technology" /></div>

	<p>Students will able to prepare the media for microbial growth to isolate and enumerate causes of food spoilage organisms, operate microscope and detect the presence of food borne pathogens present in both fresh and processed foods.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Autoclave, Colony Counter, Refrigerator, Incubator, Light Microscopes, Laminar Air Flow Chamber, Medical Centrifuge, Colorimeter, pH Meter</p>
	<br>
	<h4 class="page-title">Fruit and Vegetable Technology Laboratory</h4> 
	<p>Students will learn the manufacturing process associated with fruit and vegetable based RTS products, drying methods, osmotic dehydration process and quality evaluation of raw and processed fruit and vegetable products.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Electric Steam Boiler, Fermentation Cabinet, Refrigerator, Hot Air Oven, Humidity Chamber, Oil Bath, Retort Unit, Texture Analyzer, Tray Dryer, Hand Refractometer, Stainer, Blender</p>
	<br>
	<h4 class="page-title">Food Analysis Laboratory</h4> 
		<div class="page-img"><img src="image/dept/ftlab6.jpg" alt="Food Technology" /></div>

	<p>Students are able to analyze the macronutrients, micronutrients, pigments and other components of the food using different analytical instruments. They will also learn the process of detecting the presence of adulterants, additives and antioxidants in both solid and liquid food products.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Kjeldahl Apparatus, Dean and Stark Apparatus, UV Spectrophotometer, pH Meter, Distillation Apparatus, Magnetic Stirrer, Muffle Furnace, Moisture Balance, Soxhlet Extraction Apparatus, Stalagmometer, Viscometer, Pycnometer with Thermometer</p>
	<br>
	<h4 class="page-title">Baking & Confectionery Technology Laboratory</h4> 
		<div class="page-img"><img src="image/dept/ftlab7.jpg" alt="Food Technology" /></div>

	<p>Students will understand the manufacturing process associated with Bread, Buns, Biscuits, Cookies, Sugar and chocolate confectionery products and learn the rheological properties of flour using précised instruments.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Sedimentation Cylinder, Deck Oven, Dough Mixer, Fermentation Cabinet, Humidity Chamber, Hunter Colorimeter, Texture Analyzer, Refrigerator, Extruder, Oven Toaster Grill (OTG) Oven, Electric Blender</p>

<br>
	<h4 class="page-title">Dairy Process Technology Laboratory</h4> 
		<div class="page-img"><img src="image/dept/ftlab8.jpg" alt="Food Technology" /></div>

	<p>Students will be able to learn the manufacturing process associated with Dairy products, get familiar with adulterants and preservatives in milk and sampling techniques associated with microbiological analysis and explore the unit operations associated with dairy industry</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Gerber Centrifuge, Butyrometer, Lactometer, Refractometer, Polarimeter, milko Tester,Thermometer, Lovibond Comparator Disc, Pasteurizer, Plate Heat Exchanger, Kjeldahl Apparatus, Laminar Air Flow Chamber, Incubator</p>
<br>
<h4 class="page-title">Food Packaging Laboratory</h4> 
<div class="page-img"><img src="image/dept/ftlab9.PNG" alt="Food Technology" /></div>
	<p>Students are able to describe the testing methods for packaging material, get familiar with the type of plastic packaging material using universal testing machine for analysis of different parameters of packaging material. </p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Universal Tensile Testing Machine, Calliper, Thickness Gauge, Friction Tester, Puncture Resistance, Heat Seal Tester, Water Vapour Permeability, Cobb Tester, Burst Strength Tester</p>

<h4 class="page-title">Livestock and Marine Technology Laboratory</h4> 
<div class="page-img"><img src="image/dept/ftlab10.png" alt="Food Technology" /></div>
	<p>Students will be able to learn the testing methods for livestock and marine products, get familiar with quality analysis of meat, poultry and fish, gain experience with analysis of various components in livestock and marine products and assess lipid oxidation in meat products.</p>
	<h6 class="page-title">List of Equipment: </h6>
	<p>Vacuum oven, Laminar air flow unit, Meat mincer, Centrifuge, Weighing balance, Hot air oven, Hunter Colorimeter</p>




     
