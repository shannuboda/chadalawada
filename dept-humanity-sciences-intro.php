                        <h1 class="page-title"> Department Library</h1>
                        <p>
                            Our institution possess well equipped central library with necessary text and reference
                            books, periodicals, journals and e-resources in all the subjects related to engineering,
                            technology, management, science and humanities. Every class has one compulsory library hour
                            / week for permitting the students to access the reference books and journals.
                            Apart from the central library, every department has a library with adequate collection of
                            text and reference books and journals. The department has an in-house library which houses
                            good number of printed resources. The text and reference books, pertaining to Mathematics,
                            Physical Sciences and English are both contemporary and examination. The faculty members and
                            students have an access to these books and that serve as an aid to the academic pursuits of
                            interested candidates. Efforts are made to renew and update the collection with the latest
                            editions of reference books
                            The college has subscribed to the e-journal packages. The packages cover journals with
                            multiple accesses and download facility for all faculty, students, researchers and staff
