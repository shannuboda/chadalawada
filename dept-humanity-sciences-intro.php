                        <p>
                            Dr. M. Muthusamy, Professor & Head, Department of Humanities and Sciences obtained M.Sc
                            Degree in 1989 and M.Phil Degree in 1990 from the University of Madras. He received his Ph.D
                            from SCSVMV University, Kanchipuram in 2014, working in the field of Graph Theory. He has
                            published many papers in the International Journals. Dr. M. Muthusamy has got more than 30
                            years of experience in teaching and Research. He has been with Rajalakshmi Engineering
                            College since 2001.
                            <br /><br />
                            The department of Humanities and Sciences was established in the year 1997 and it has
                            emerged as a department pursuing excellence. The department has a team of highly-qualified
                            and well-experienced faculty members and non-teaching staff. The department has 24 faculty
                            members with Doctoral degree and 28 faculty members are pursuing Doctoral degree. Many
                            faculty members are experts in some of the emerging fields of English, Mathematics and
                            Physical sciences and have made significant contributions in their own fields. The aim of
                            the department is to broadly educate the aspiring engineering students in the basics of
                            Mathematics, Physics, Chemistry & English which serve as a platform for the growth of
                            students in their further study of Technical subjects. The strength of the department lies
                            in the perfect synchronization of knowledge and skills. The major motivating factor of the
                            department is the commitment and team work that are being exhibited in the teaching –
                            learning process. Extra care is given by offering special coaching classes during weekends
                            and crash courses at the time of the University Examinations/End Semester Examination, which
                            are tailor – made courses to suit the needs of all types of students. Adding a feather to
                            the cap, the department acclaims the recognition from the Anna University as a research
                            centre in Mathematics & Chemistry.
                            <br /><br />
                            The department has spacious, well equipped and fully furnished laboratories in Physics,
                            Chemistry and Communication skills. The Physics and Chemistry laboratories have
                            sophisticated major and minor equipments which cater to the academic needs of undergraduate
                            and post graduate students of the institution. The laboratory also supports students in
                            need, to complete their final year projects as well. The Communication skills laboratory is
                            well equipped to meet the challenges of the ever-changing needs of the globalization, in
                            terms of acquiring language skills. Students are trained in group discussions, presentations
                            and Interview skills in order to making them employable. The department has an in-house
                            library which houses good number of printed resources. The text and reference books,
                            pertaining to Mathematics, Physical Sciences and English are both contemporary and
                            examination - oriented. The faculty members and students have an access to these books and
                            that serve as an aid to the academic pursuits of interested candidates. Efforts are made to
                            renew and update the collection with the latest editions of reference books. The Department
                            has made tremendous progress technologically and every faculty member is provided access to
                            internet. This facilitates sharing of knowledge and information that play a significant role
                            in upgrading and enriching research works. Teaching and Research should go hand in hand; the
                            faculty members of the department actively participate and present papers in conferences and
                            attend programmes such as workshop, seminar, FDP etc.. Various Guest lectures and Industrial
                            visits are arranged for the benefit of students to acquire practical knowledge in their
                            course of study. In-plant training is also arranged for the students to gain industrial
                            exposure.
                            <br /><br />
                            <strong>Vision</strong>
                            <br /><br />
                            To become a Department of Excellence in Mathematical education, Physical Sciences, Research
                            and Development and Setting the standards of Excellence in Communication skills and
                            preparing the learners to meet the challenging trends in the professional career.
