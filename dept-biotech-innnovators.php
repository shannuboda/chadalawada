                <strong>Tamil Nadu Student Innovators Programme</strong>
                <br><br>
                Tamil Nadu Student Innovators Programme is an initiative of EDII-TN, introduced in 2016, to encourage student innovators/entrepreneurs from all over Tamil Nadu. The student teams are provided opportunity to give their innovative ideas as a solution to identified validated problem statements in the field of Agriculture Industry, Smart Cities, Health Care, Fin-Tech and Automobile sectors. The programme includes series of events like awareness and ideation programmes, boot camps and final pitch. Innovative ideas are selected by a panel of Jury from the Startup Ecosystem for the award of Rs. 1.00 lakh each for further development of their prototype and also to commercialize the product. They are also introduced to angel investors who are ready to adopt and invest in their startups.
