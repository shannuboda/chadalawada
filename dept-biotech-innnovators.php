                    <strong>SPARSH Social Innovator Fellow</strong>
                    <br><br>
                    SPARSH is the social innovation programme of BIRAC aimed towards finding innovative solutions to society’s most pressing social problems through biotechnological interventions. SIIP (Social Innovation Immersion Program), a component of SPARSH scheme, is BIRAC’s social Innovation fellowship/award program aimed at creating a pool of biotech “Social Innovators” who could identify the needs & gaps within communities and then can help bridge the gaps either through an innovative product development or services. The Program is implemented through SPARSH Centres. Selected Fellows, designated as ‘SPARSH Social Innovators/ SPARSH Fellows’, would receive a fellowship from BIRAC (amounting to INR 50,000/month for 18 months) and a onetime mini-kick start grant of INR 5 lakhs per fellow, which will be provided during the course of the fellowship.
