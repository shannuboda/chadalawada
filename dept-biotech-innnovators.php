                <strong>DBT ABLE BEST</strong>
                <br><br>
                Association of Biotechnology Led Enterprises - ABLE is a not-for-profit pan-India forum that represents the Indian Biotechnology Sector. The primary focus of ABLE is to catalyse a symbiotic interface between the industry, the government, academic and research institutes and domestic and international investors. BEST-India (Biotechnology Entrepreneurship Student Teams) sponsored by DBT and administered by ABLE - India is a program aimed at encouraging young postgraduate and doctoral students in developing biotechnology entrepreneurship and exposing them to issues involved in commercialization of bio-science. Three teams from the Department of Biotech have been winners at DBT-ABLE BEST India in 2017 and 2018. One team was shortlisted among the top 20 teams in 2019.
