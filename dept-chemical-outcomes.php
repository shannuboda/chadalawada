          <strong>
            Program Outcomes (POs)
          </strong>
        </h3>
        <p>
          Engineering Graduates will be able to <br><br>
          <strong>
            1. Engineering Knowledge:
          </strong>
          Apply the knowledge of mathematics, science, and engineering fundamentals, to solve the complex chemical engineering problems <br><br>
          <strong>
            2. Problem analysis:
          </strong>
          Identify, formulate, review research literature, and analyze complex chemical engineering problems reaching substantiated conclusions using first principles of mathematics, natural sciences and engineering sciences. <br><br>
          <strong>
            3. Design/development of solutions:
          </strong>
          Design solutions for complex chemical engineering problems and design system components or process that meet the specified needs with appropriate consideration for the public health and safety, and the cultural, societal and environmental considerations. <br><br>
          <strong>
            4. Conduct investigations of complex problems:
          </strong>
          Use research based knowledge and research methods including design of experiments, analysis and interpretation of data, and synthesis of the information to proceed valid conclusions. <br><br>
          <strong>
            5. Modern tool usage:
          </strong>
          Create, select and apply appropriate techniques, resources and modern engineering and IT tools including prediction and modeling to complex chemical engineering activities with an understanding of the limitations. <br><br>
          <strong>
            6. The engineer and society:
          </strong>
          Apply reasoning informed by the contextual knowledge to assess societal, health, safety, legal and cultural issues and the consequent responsibilities relevant to the professional chemical engineering practice. <br><br>
          <strong>
            7. Environment and sustainability:
          </strong>
          Understand the impact of the professional chemical engineering solutions in societal and environmental contexts, and demonstrate the knowledge of and need for sustainable development. <br><br>
          <strong>
            8. Ethics:
          </strong>
          Apply ethical principles and commit to professional ethics and responsibilities and norms of the chemical engineering practice. <br><br>
          <strong>
            9. Individual and team work:
          </strong>
          Function effectively as an individual and as a member or leader in diverse teams, and in multidisciplinary settings. <br><br>
          <strong>
            10. Communication:
          </strong>
          Communicate effectively on complex chemical engineering activities with the engineering community and with society at large, such as, being able to comprehend and write effective reports and design documentation, make effective presentations, and give and receive clear instructions. <br><br>
          <strong>
            11. Project management and finance:
          </strong>
          Demonstrate knowledge and understanding of the engineering and management principles and apply these to oneâ€™s own work, as a member and leader in a team, to manage projects and in multidisciplinary environments. <br><br>
          <strong>
            12. Life-long learning:
          </strong>
          Recognize the need for, and have the preparation and ability to engage in independent and life-long learning in the broadest context of technological changes in chemical engineering.
        </p>
