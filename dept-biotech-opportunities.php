                    <br>
                    <strong>State Level Initiatives</strong><br>
                    Many state governments have also realized the benefits/importance of this industry. State governments namely Andhra Pradesh, Tamil Nadu, Karnataka and Maharashtra have begun to formulate their policies, develop R&D centers to encourage and nurture the Biotech industry. Following is a brief outline of some of the initiatives taken by a few state governments:<br>
                    <strong>Andhra Pradesh</strong>
                    <ul>
                        <li> Government of Andhra Pradesh, in collaboration with the ICICI Limited, has set up a knowledge Park near Hyderabad.</li>
                        <li>Development of 'Genome Valley' and Biotech Park with state-of-the art features.</li>
                        <li>The Government has initiated many business friendly policies such as 'Single Window Clearance' mechanism geared to relieve the problem of red tape.</li>
                    </ul>
                    <strong>Tamil Nadu</strong>
                    <ul>
                        <li> The Government of Tamil Nadu is facilitating in the setting up of the biotechnology enterprise zones (bio-valleys) along the lines of Silicon Valley to exploit the bio resources of the state.</li>
                        <li>Four state-of-the art biotech parks, a bioinformatics and genome center will be established, each of which would be leveraging the bioresources of the agro-ecological zones of Tamil Nadu.</li>
                    </ul>
                    <br>
                    <strong>Maharashtra</strong>
                    <ul>
                        <li>Has an excellent intellectual infrastructure. Through nearly 1000 institutions, it produces around 163,000 trained technical personnel each year. Some of the best Centers of excellence in India are present in Maharashtra.</li>
                        <li> The government is also promoting biotech parks, R&D centers, and pilot plant facilities for underway contract research by putting equity stakes in such projects.</li>
                    </ul>
                    <br>
                    <strong>Karnataka</strong>
                    <ul>
                        <li> The Karnataka government has announced a biotech policy to promote this sector and is setting up an institute for bioinformatics in Bangalore</li>
                        <li> In addition the state government is also creating a biotechnology fund that will have inflows from the biotech companies. This could be used for incubation of new projects and promotion of the sector in the state.</li>
                        <li>Karnataka has planned to launch India's first state sponsored biotechnology venture capital fund to boost their initiatives.</li>
                    </ul>
                    <br>
                    <strong>Himachal Pradesh</strong>
                    <ul>
                        <li> Himachal Pradesh has prepared a blue print for promotion of biotechnology industries in the state which includes setting up of biotechnology parks, conservation and exploitation of bio-resources, intensification of R&D, and promoting biotechnology entrepreneurship through tax concessions and relaxed labor laws.</li>
                        <li> It is also proposed to provide research based support to the private companies in form of providing for instance, access to a data base of bio-resources which is being developed along with separate entries of endangered medicinal plants.</li>
                        <li> The State government has recently announced 100% tax holidays for all biotechnology products up to the year 2012.</li>
                    </ul>
                    <br>
                    <strong>India's Biotech Industry:</strong><br>
                    Segment Review: The Indian Biotech industry can be divided into different segments. Following is a review of largest and the fastest growing segments of the Biotech industry in India.<br>
                    <br>
                    <strong>Medical Biotech Segment</strong><br>
                    The Indian pharmaceutical market is growing very rapidly. According to a study by Mckinsey, Indian Pharma industry is expected to grow to an innovation-led US $25 billion industry by 2010 with a market capitalization of almost US $150 billion from the current US $5 billion generic based drug industry. The vaccine market is expected to grow by roughly 20%. <strong>Agri Biotech Segment</strong>
                    <ul>
                        <li> India being the second largest food producer, offers a huge market for biotechnology products, especially agribiotech products.</li>
                        <li> India has an excellent scientific infrastructure in agriculture, rich bio-diversity and skilled and low cost human-power.</li>
                        <li>In a report by Ernst & Young it is expected that the Nutraceuticals market is roughly US $532- 638 million presently and growing.</li>
                        <li>With its 8000 kilometer of coastline including Andaman & Nicobar and Lakshwadeep islands, India has a rich aqua culture and its Marine resource development holds great potential.</li>
                    </ul>
                    <br>
                    <strong>Services Segment</strong><br>
                    <ul>
                        <li> With increasing number of pharmaceutical companies finding it difficult to conduct entire drug discovery process-in-house they are looking for ways to minimize costs. India has become a very attractive base as the cost of infrastructure is relatively lower compared to other nations.</li>
                        <li> Foreign companies also benefit from cheaper qualified workforce available in India.</li>
                        <li> India produces enough qualified graduates each year thus companies looking to expand their operations can easily do so without facing a shortage in labor.</li>
                    </ul>
