                    was constituted to strengthen research activities, enhance industry interaction and to foster
                    industry relevant research. <br />
                    <br />
                    CSRC provides support and guidance to enable faculty and students to take up research and innovative
                    development relevant to industry as well as society.<br />
                    <br />
                    The CSRC is headed by Dr. R. Sundar and ably guided by Dr. C. R. Muthukrishnan, Chairman,
                    CSRC.<br />
                    <br />
                    <strong>Major goals of CSRC include the following:</strong>
                    <ul>
