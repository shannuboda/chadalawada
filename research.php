                    <h1 class="page-title">Machine Vision</h1>
                    <section class="vision-mission">
                        <p><strong>Vision</strong> <br>
                            To be a globally recognised centre of excellence in the field of Machine Vision
                            providing/imparting quality education, interdisciplinary research and promote further
                            collaboration with various industrial partners
                        </p>
                        <p>
                            <strong>Mission</strong>
                        </p>
                        <ul>
                            <li>To promote scientific discovery and the invention of smart systems and Intelligent
                                Technologies</li>
                            <li>Interdisciplinary research and education to nurture future experts in IoT research by
                                imparting interdisciplinary knowledge, innovative ideas, critical thinking and practical
                                skills.</li>
                            <li>To undertake cutting edge research and education in the areas of networking systems and
                                computational intelligence, and to derive evolving, comprehensive and integrative
                                methods for solving various societal problems to make an impact on sustainable living.
                            </li>
                            <li>To be the premier source of well-educated network and IoT engineers, fostering strong
                                academic, industrial, and social ties, and thereby achieving significant societal
                                impact.</li>
                            <li>To carry out innovative research and development that contributes to improve social
                                impacts in the Country.</li>
                            <li>To build the capacity of technical personnel working in the various institutions.</li>
                            <li>To provide consultancy, training, and outreach services for stakeholders in the region
                                including social institutions.</li>
                        </ul>

                        <p>
                            <strong>Objectives and Research Activities</strong> <br>
                            To realize its vision and accomplish its mission, the centre will have the following
                            specific objectives: <br>
                            To carry out advanced research activities in the area of Machine Vision cater to the needs
                            of the industry and society (in general).
                        </p>
                        <ul>
                            <li>Conducting training programmes in the related areas for professional skill development.
                            </li>
                            <li>Generating Intellectual Properties (IP) in terms of patents and high quality technical
                                publications.</li>
                            <li>Conducting consultancy works for industries covering, but not limited to,
                                transportation, manufacturing, healthcare, agriculture etc.</li>
                            <li>Patenting and transfer of technology</li>
                            <li>Helping and guiding start-ups in relevant areas.</li>
                            <li>To enhance the practical knowledge and to develop research culture among students in the
                                field of Image and Video Processing using Machine Learning.</li>
                            <li>To provide internships and placement oriented training to students.</li>
                            <li>To provide training for faculty members in thrust areas like Digital Image Processing,
                                Embedded Development, Mechatronics, Robotics and Automation and Machine Learning. Also
                                Software’s like MatLab, LabVIEW, Solidworks, Auto Cad.</li>
                            <li>Offer PhD courses in relevant areas of expertise. Also, to provide customized/flexible
                                PhD Courses to student(s) of any department of the institute pursuing/ intended to
                                pursue his/her thesis on specific problems in Digital image Processing and machine
                                Learning.</li>
                            <li>To enter into collaborative research engagements with Extra-mural funding agencies and
                                Industrial Organizations for better understanding of issues of scientific and
                                technological management between different stakeholders.</li>
                            <li>Using this as a general research facility to take up new research projects from industry
                                and other bodies.</li>
                        </ul>

