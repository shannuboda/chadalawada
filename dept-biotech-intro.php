                        <h1 class="page-title">Beyond the Classroom</h1>
                        <div class="clearfix"></div>
                        <p>In tune with the vision of the college to bring out globally competent professionals with a
                            commitment to serve the society, the budding engineers of REC are faithfully involved in
                            community services through voluntarily asked to undergo Personality and Character
                            Development Programmes for a minimum period of 80 hours. There are two wings to this
                            programme and students may choose one club from each wing. One wing consists of pan national
                            groups like ENACTUS, NSS, YRC, ROTARACT, CII-YI and NSO while the other wing is made of
                            college level clusters like Debating Union, Atrium, Yaazh Mandram, Ethics Club, EDC and
                            STEM. Their contributions are appreciated by the voluntary organizations<br><br>
                            <strong>Quality jobs: </strong>Employability Training camps, Industrial visits, guest
                            lectures, in-plant trainings and effective practical sessions are organized to obtain
                            quality jobs in core areas.<br><br>
                            <strong>Entrepreneurship –</strong> Students&rsquo; entrepreneurial skills are honed through
                            Entrepreneurship Development Cell (Kriya), which also helps the economically weaker students
                            to &lsquo;earn while they learn&rsquo;. The EDC has brought in various innovations and the
                            students work for it with commitment and dedication. The EDC guides them to apply for
                            external funds and patents. EDC of the college functions in collaboration with National
                            Entrepreneurship Network and CII. With the support of the management, an Aavin parlour
                            namely <em>Melting Point</em> which is a registered refreshment outlet of the TCMPF and also
                            <em>Boiling Point</em> which is a registered coffee outlet of LAVAZZA are business
                            administered and run by students completely. The Melting point has recently been the winners
                            of NEN – TATA FIRST DOT judges choice of TOP 5 STUDENTS START UPS in India.<br><br>
                            <strong>Innovations:</strong> Innovative Project Cell (IPC) of the college organizes iQuest
                            – a contest for innovative ideas and projects every year for the benefit of the students.
                            The winners of this talent hunt are encouraged to take up their ideas for further
                            improvement at IIM-PALS and DBT-ABLE BEST programmes that encourage student start
                            ups.<br><br>
                            <strong>Research Aptitude</strong>: Research aptitude of students is kindled right from the
                            first year of graduation by encouraging students for course relevant in-house projects and
                            final semester projects and guidance in preparing and presenting technical papers in
                            seminars and conferences. <br><br>
                            <strong>Journal Club: </strong>The Journal Club of the Department of Biotechnology (JC
                            BT-REC) is an initiative to assist and inspire young undergraduates in their scientific
                            journey. It was launched in September 2016. It promotes research among students. It is a
                            forum for the budding scientists in the field of Biotechnology to improve their fundamentals
                            and share their ideas and experience for an enhanced research culture. The objectives of the
                            club include the following:
                        </p>
                        <ol>
                            <li>To promote research interest among students</li>
                            <li>Give guidance on how to write a review paper</li>
                            <li>Conducting workshops for conceptual understanding</li>
                            <li>Learning how to design an experiment / project</li>
                            <li>To help students in publishing their work in reputed journals</li>
                            <li>Taking up projects to the next level for commercialization and delving deeper into more
                                basic questions</li>
                            <li>To investigate the possibility of preparing student project proposals</li>
                            <li>To develop an e- library for cultivating a habit of reading popular science literature
                            </li>
                        </ol>
                        <p><strong>BT Society: </strong>The BT Society (BTS) is a student initiative that was started in
                            2015 to update the students on the current scenario in the field of Biotechnology. The
                            Society brings celebrated industrialists, entrepreneurs to interact with students. The
                            experts discuss about the knowledge gap that needs to be bridged in various sectors of
                            biotechnology. Students accordingly take up various internship / training programmes in
                            topics that are beyond the syllabus but indispensable when applying for higher studies or
                            job applications. <br>
                            The elected members of the team organize various events and discuss activities to be carried
                            further. Brainstorming sessions, quiz, model creation are done to keep students always in
                            active mode thinking out of the box in the subjects. The students also celebrate the
                            awareness days and also organized camps benefitting the entire college through this forum.
                            Conferences are also organized which gives hands on experience and organizing
                            responsibilities to students.</p>
                        <p><strong>Biosnippets: </strong>The Department of Biotechnology releases a technical Newsletter
                            under the heading of 'Biosnippets'. Each issue of Biosnippets focuses on different aspects
                            of Biotechnology as its theme. The magazine has its team of exclusively student editors who
                            develop technical content related to recent scientific developments, biomimetics,
                            bioillustrations, technical puzzles, popular sections like myth busters and reviews books
                            and movies (biotech related). The magazine also solicits contributions from its readers
                            which include science articles, poems, sketches etc. The magazine seeks to encourage both
                            faculty and students, by highlighting their achievements like research grants secured, PhD
                            degrees obtained, papers published, trainings attended, university ranks attained and prizes
                            &amp; medals won at various conferences, symposiums, sports, cultural events, etc. It is
                            hoped that shining a light on such achievements would also shine a path to excellence for
                            future students. </p>
