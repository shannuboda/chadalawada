                        <p><strong>Fellowships and Internships</strong><br>
                            Department of Biotechnology, Rajalakshmi Engineering College has a rich tradition of
                            students winning prestigious National and International Fellowships and Internships. With
                            every year we have had ever burgeoning number of students winning the competition for
                            positions among their national peers.<br><br>
                            <strong>International Fellowships</strong>
                            <br><br>
                            Mitacs Globalink Research Internship is a competitive initiative for top ranked
                            international undergraduate applicants from the following countries and regions: Australia,
                            Brazil, China, France, Germany, Hong Kong, India, Mexico, Taiwan, Tunisia, Ukraine, United
                            Kingdom and the United States to participate in a 12-week research internship under the
                            supervision of Canadian university faculty members in a variety of academic disciplines,
                            from science, engineering and mathematics to the humanities and social sciences.
                            <br><br>
                            The India Connect @NTU (IC@N) Visiting Research Student Programme for 2 to 6 months for
                            undergraduate and Master’s level graduate students from India is the flagship programme of
                            IC@N. This research programme was introduced in 2016 to enable students of Indian
                            universities to pursue their research interests at NTU by working under the supervision of
                            NTU faculty, developing a strong research expertise in specific research areas, experiencing
                            a multicultural life, developing a strong network of friends and a better understanding of
                            Singapore.
                            <br><br>
                            The Khorana Program is a collaboration between the Department of Biotechnology (GoI),
                            University of Wisconsin (UW) Madison, USA and the Indo-US Science and Technology Forum
                            (IUSSTF) to nurture contacts between students of biotechnology and biomedical sciences from
                            India and the US, through a joint training programme in biotechnology and allied areas. The
                            programme acts as a key catalyst to encourage students to take up research as a career.
