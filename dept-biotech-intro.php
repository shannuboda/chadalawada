                        <h1 class="page-title">Opportunities</h1>
                        <p> After becoming an IT bellwether, India is now shifting its focus to the most promising
                            industry of the future, Biotechnology. With its large pool of scientific talent, world-class
                            information technology industry, and vibrant pharmaceutical sector, India is well positioned
                            to emerge as a significant player in the global biotech arena.<br>
                            <br>
                            Biotechnology is perceived as a revolution throughout the world. Scientists, through
                            Research and Development (R&D), have developed and are continuing to develop cures for
                            diseases that have affected people for decades and even centuries. Scientists recently, have
                            also, clinically developed crops that can withstand the brutalities of weather changes,
                            helping poor farmers of the developing countries to retain their yield and increase their
                            output manifold. Biotech, considered a boon by some, provides great hope to many around the
                            world, and its benefits are and will be realized by more and more people over the years.
                            <br>
                            <br>
                            On the threshold of this new revolution, numerous companies have sprung up to take a piece
                            of the exponentially growing Biotech market worldwide. The ever-decreasing physical
                            boundaries enable biotech companies from the West to tap large markets around the world.<br>
                            <br>
                            India to this extent holds a good advantage over many other countries of the world. With its
                            large population of over a billion people there is a huge market for products and services.
                            India's population has a very interesting demography that creates almost a perfect
                            environment for biotech companies to shift bases here. In addition the Indian sub-continent,
                            which occupies only 2.4% of the total global surface area, has the most varied species of
                            flora and fauna. A study shows that, in percentage terms, India has about 7.6% of total
                            mammal species, 12.6% of bird species, 11.7% of fishes and roughly 6.0% of total flowering
                            plants that are present in the world. Biotech companies, by moving to India, can utilize
                            this immense Bio-diversity, can easily find samples and, also conduct field research much
                            more efficiently. Adding to this, India has one of the largest agriculture sectors in the
                            world, and varied climatic zones that can help in research and development of different
                            agri-biotech products applicable worldwide.<br>
                            <br>
                            <strong>Indian Advantage:</strong><br>
                            India, today, holds a small share of the global biotech market, but has all the capabilities
                            to become a dominant player. The consumption of biotech products in India is expected to
                            quadruple in the next decade. The human and animal segment of the industry alone is growing
                            by at least 20%.<br>
                            <br>
                            India has a rich human capital, which is the strongest asset for this knowledge-based
                            industry. India has a large English speaking base and, according to Confederation of Indian
                            Industry estimates, produces roughly 2.5 million graduates in IT, engineering and life
                            sciences, about 650,000 postgraduates and nearly 1500 PhDs qualified in biosciences and
                            engineering each year. India has proved its competency in selected areas of biotechnology
                            such as, to name a few: capacity in bioprocess engineering, skills in gene manipulation of
                            microbes and animal cells, capacity in downstream processing and isolation methods, and its
                            competence in recombinant DNA technology of plants and animals. India has also allowed
                            assisted stem cell research that permits researchers to use embryos from fertility clinics
                            upon informed consent of the donors, thus giving it a clear head start in this new and
                            promising field in Biotech. Clearly, India has the strength and capabilities in this
                            industry, and a definite advantage to forge ahead and become the chosen location for many
                            biotech companies looking for large markets and low cost qualified manpower to work in their
                            R&D division.<br>
                            <br>
                            It is estimated that vaccines, contract research, agriculture and human health sectors
                            comprise as much as two thirds of the total market. It is further estimated that health care
                            products would dominate the Indian biotech market, roughly 40% of the total market by the
                            year 2010 followed by agriculture of about 30%. It is also estimated that contract research
                            and bioinformatics would pick up and account for as much as 25% of the biotech market. An
                            estimation by CII shows that Agri-Biotech would see growth rates of as much as 60%,
                            Diagnostic and Therapeutics of about 25% and Vaccines of about 15%. These figures clearly
                            indicate the prospects of the Biotech industry in India.<br>
                            <br>
                            <strong>Government and State Initiatives</strong><br>
                            The Government of India realized the potential and benefits of this industry at an early
                            stage and formed the Department of Biotechnology in 1986 that has now become the central
                            agency, responsible for policy, promotion of R&D and for international cooperation and
                            manufacturing activities. Some of the initiatives taken by the Government of India are as
                            follows:<br>
                        <ul>
                            <li>The Government of India has been increasing its outlays to provide financial support to
                                this industry. Government of India is also setting up a venture capital fund, to support
                                small and medium enterprises.</li>
                            <li>Good regulatory framework has been set up for approval of GM crops and rDNA products.
                                Recently, the Government of India decided to make changes to the Drugs & Cosmetics Act
                                to make it more globally compatible.</li>
                            <li>Indian Patents Bill recently passed by the Parliament allowing 20-year patent term,
                                inline with provisions made by WTO and TRIPS.</li>
                            <li>India has a sound and widely acknowledged framework of bio-safety guidelines to deal
                                with evaluation, monitoring and release of genetically engineered organisms and there
                                are more than 106 institutional bio-safety committees.</li>
                            <li>The initiatives taken by both Central government and State governments have given a big
                                boost to the Biotech industry in India. Foreign companies looking for new markets and to
                                expand facilities to much more economical locations can find India ever more open and
                                responsive to their needs.</li>
                        </ul>
