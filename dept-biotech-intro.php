                <h1 class="page-title"> Department of Biotechnology</h1>
          <strong>Programmes Offered</strong>
          <br>UG - B.Tech. Biotechnology
          <br>PG - M.Tech. Biotechnology<br>
Ph.D. - Biotechnology<br><br>
                <div class="page-img"><img src="image/dept/Biotechnology.jpg" alt="Biotechnology" /></div>
                <p>
                Engineering is imagination emerged through the aid of science to a plan on paper! Then it moves to realization in stone or metal or energy or even DNA and protein. Then it brings jobs and home to men. It elevates the standards of living. That’s the Engineer’s highest privilege. 

                <br /><br />
                Biotechnology is a subject for those who are passionate about science and to all those who thirst for knowledge. Biotechnology has been running in our veins for centuries now. No other industry is better placed to enhance the quality of life and respond to society’s challenges of tackling an ageing and ever increasing population, healthcare choice and affordability, resource efficiency, food security, climate change and energy shortages. Biotech can and will pay economic, social and environmental dividends. 

                <br /><br />
                <b>Established in 2002</b>, the Department of Biotechnology offers B.Tech and M.Tech in Biotechnology, both of which are <b>NBA accredited</b> and the department has been granted permanent affiliation by Anna University. The department is also an Anna University recognised research centre offering Ph.D programme. Dedicated and experienced faculty members, with doctorate and post-doctoral qualifications sit on the editorial boards of prestigious peer reviewed national and international journals, are members of Boards of Studies, members of Syllabus Committees, Research Supervisors, Doctoral Committee members among other responsibilities. Our faculty members have been recognized all over the world for their quality and ability. The Department has been bestowed with <b>Teaching Awards in Engineering by Staffordshire University and Education Matters</b>.

                <br /><br />
                Scientific research in the department encompasses Cancer Biology, Bioinformatics, Environmental Engineering, Immunotechnology, Ethnopharmacology and Nanobiotechnology. Faculty has been granted research funding to the tune of INR 1 crore by various funding agencies. The department has established state of the art laboratories to develop natural polymers, biopesticides and food preservatives. The department also has Memorandums of Understanding with leading biotech companies like GE Health Care India and Eppendorf India Limited, to provide internationally recognised certificate training programmes to our students. The department has also established close relationship with central government funded bioincubators in the city that help provide the students with a taste for entrepreneurship.
                
                <br /><br />
                The department also supports its very own BT Society, Journal Club and Biosnippets magazine to explore different modes of inculcating knowledge. Some of the noteworthy products developed by our students include nanospun biomaterials for wound healing, portable diagnostic kits, Nano-herbal formulations for treatment of diabetes, atherosclerosis, myocardial infarction and supplements against drug induced toxicities. We are also focussed on environmental protection, by detection of environmental toxins, effluent treatment using biomaterials, phytoremediation, computational drug design and network biology.
                
                <br /><br />
                The department has produced <b>159 University ranks including 7 gold medals</b> in UG and PG since inception. On the international stage our students have been the proud recipients of Indo-US Khorana Fellowship, NTU-India Connect Research Internship and MITACS Global Research Internship while teams from the department have successively bagged bronze and silver medals at iGEM Giant Jamboree held at MIT, Boston. On homeground, our students have received IAS/INSA/NASI Joint Research Fellowships, INSPIRE Fellowship, AcSIR Fellowship, JNCASR Fellowship and IITM-RuTAG Fellowship. Student teams from the department mentored by our proficient faculty have blazed their trails by winning DBT-ABLE Biotechnology Entrepreneurship Student Teams Award and IITM-PALS InnoWAH Innovation Challenge. Graduates from our department have gone on to pursue direct Ph.D in IITs, IISc and top notch Universities abroad. Our alumni are currently serving in NIH (USA), FDA (USA), GENTEK (USA), Princeton University (USA), BIOCON, NOVOZYMES, EPPENDORF to name a few.
