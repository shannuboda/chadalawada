                        <br>
                        <strong>India's Biotech Industry:</strong><br>
                        Segment Review: The Indian Biotech industry can be divided into different segments. Following is
                        a review of largest and the fastest growing segments of the Biotech industry in India.<br>
                        <br>
                        <strong>Medical Biotech Segment</strong><br>
                        The Indian pharmaceutical market is growing very rapidly. According to a study by Mckinsey,
                        Indian Pharma industry is expected to grow to an innovation-led US $25 billion industry by 2010
                        with a market capitalization of almost US $150 billion from the current US $5 billion generic
                        based drug industry. The vaccine market is expected to grow by roughly 20%. <strong>Agri Biotech
                            Segment</strong>
                        <ul>
                            <li> India being the second largest food producer, offers a huge market for biotechnology
                                products, especially agribiotech products.</li>
                            <li> India has an excellent scientific infrastructure in agriculture, rich bio-diversity and
                                skilled and low cost human-power.</li>
                            <li>In a report by Ernst & Young it is expected that the Nutraceuticals market is roughly US
                                $532- 638 million presently and growing.</li>
                            <li>With its 8000 kilometer of coastline including Andaman & Nicobar and Lakshwadeep
                                islands, India has a rich aqua culture and its Marine resource development holds great
                                potential.</li>
                        </ul>
                        <br>
                        <strong>Services Segment</strong><br>
                        <ul>
                            <li> With increasing number of pharmaceutical companies finding it difficult to conduct
                                entire drug discovery process-in-house they are looking for ways to minimize costs.
