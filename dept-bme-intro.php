                                regular curriculum. The department provides lab facilities after college hours to
                                students who are interested in working on their innovative ideas or projects. To assist
                                students and help them in their aspirations, highly knowledgeable lab assistants are
                                also present always. The labs in the department cater to the needs of students looking
