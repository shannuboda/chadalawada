                        <h1 class="page-title">Opportunities</h1>
                        <p>
                            Biomedical engineers use their expertise in biology, medicine, physics, mathematics,
                            engineering science and communication to make the world a healthier place. The challenges
                            created by the diversity and complexity of living systems require creative, knowledgeable,
                            and imaginative people working in teams of physicians, scientists, engineers, and even
                            business folk to monitor, restore and enhance normal body function. The biomedical engineer
                            is ideally trained to work at the intersection of science, medicine and mathematics to solve
                            biological and medical problems.<br /><br />

                            Biomedical engineers use their engineering and science backgrounds to design the next
                            generation of systems and treatments that will advance the quality of life for patients.
                            They leave an impact through the creation of medical devices that detect and treat disease,
                            the invention of materials that can be used to treat illness in the body, and by designing
                            complex computer models to develop the next generation of disease-fighting
                            drugs.<br /><br />

                            Biomedical engineers are responsible for the creation of artificial organs, automated
                            patient monitoring, blood chemistry sensors, advanced therapeutic and surgical devices,
                            application of expert systems and artificial intelligence to clinical decision making,
                            design of optimal clinical laboratories, medical imaging systems, computer modeling of
                            physiological systems, biomaterials design, and biomechanics for injury and wound healing,
                            among many others.
                            <br /><br />
                            Biomedical engineers respond to societal needs and provide solutions to engineer and a
                            better and healthier world.
                        </p>

