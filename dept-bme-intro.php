                    <b>Introduction</b><br><br>
                    Biomedical Engineering is the application of Engineering concepts and techniques in the field of Medicine. It combines the problem solving capability of engineers with the medical expertise of physicians to enhance the quality of life through health care process. Biomedical engineers bridge the medical and engineering disciplines to provide an overall enhancement in the quality of health care and products.<br /><br />

                    Rajalakshmi Engineering College has established the Department of Biomedical Engineering in the year 2006.Department of Biomedical Engineering offers UG in Biomedical Engineering and PG in Medical Electronics. Faculty members are doing in-house research and publishing papers in peer reviewed international journals and conferences.Our facultymembers have the following areas of expertise such as: Biosignal and Medical image processing, Rehabilitation engineering, Biomedical instrumentation.<br /><br />

                    The department has signed MoU’s with industries and research organizations for faculty training, students training and placement. Visits to hospitals, Equipment manufacturing & maintenance, Industries and Research Centres form part of our core curriculum, so that the students get first-hand knowledge in these areas. Industrial training and Project training are arranged for all students to develop their practical and working knowledge.<br /><br />

                    Each student is encouraged to develop innovative products on their own as part of Innovative Projects and the best are presented in intercollegiate competitions. Our students have won prizes in the national level innovation design contest. Keeping the employability of the graduates in mind, we offer value added labs using latest software and also offer special programmes on communication skills and soft skills so that they are technical experts as well as confident personalities in today's competitive job market. <br /><br />

                    The department has organized many National seminars, Workshops and Faculty development programmes on various topics of biomedical engineering in association with All India Council for Technical Education (AICTE), Indian Council for Medical Research (ICMR) and Atomic Energy and Regulatory Board (AERB), India. <br /><br />

                    The alumni of the Department of Biomedical Engineering are working in reputed healthcare industries in India and abroad. The list of few companies are; Allengers, CURA health care, Stryker, L&T; ECC, HRS Navigation, Phoneix Medical Systems, TCS-Medical Division, Skanray Technologies, Hulhumale Hospital, Medtronic, MIOT hospitals, BPL Healthcare, Agappe Diagnostics, Faber-Sindori-Apollo Groups, TransAsia, Zifo RnD solutions and HCL R&D.;<br /><br />

                    Our alumni are pursuing higher education in renowned Universities across the globe and some of them are: University of California, University of South Florida-USA, Hochschule Furtwangen University, Tomsk Ploytechnic University, UMEA Univeristy-Sweden and Arizona state University, USA.
                    <br /><br />

