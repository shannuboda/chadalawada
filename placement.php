<h1 class="page-title">Employability Training Camp</h1>
    <div class="clearfix"></div>
      <p>
   The Employability Training Camp is a unique one of a kind value addition programme offered to our students from the Second year onwards. This camp, which is run on a residential mode is offered completely free of cost. It is a matter of pride that this programme is one of the premier events in the Institution and every student looks forward eagerly to be part of this event.<br /><br />

The students who are part of the camp are put through a set of pre-camp screening tests and their exact training requirements mapped. Following the Ice Breaker session on the inaugural day, our expert trainers who are the best in the Industry put our students through their paces through a uniquely developed curriculum, which is aligned towards the ever-changing demands of the Corporate.
<br /><br />
The students are also continuously monitored for their performance with daily online tests, camp diary etc and corrective actions are implemented immediately. The students also have the benefit of interacting with top executives from various industries on a daily basis in "Meet the Industry" programme conducted every evening. In this way every student is given the opportunities to understand the difference between an academic ambience and the corporate reality. The camp also serves as a platform for students to learn and practice Yoga as every morning session starts with a lengthy but useful Yoga session to help them manage the stress levels built up during the course of the camp stay. In this way the residential Employability Training Programme facilitates the student to become employable by the Industry.<br /><br />

