<h2 class="page-title">Procedure</h2>     <p>
<strong>Why Corporates prefer our students ?</strong>
<ul>
<li>
Campus recruitment for final year students start from June onwards every year. Recruitment after passing out of the campus will also be done depending on the availability of non placed students.</li>
<li>Interested recruiters are requested to mail the following details to placement@rajalakshmi.edu.in</li>
<ul>
<li>Job Profile</li>
<li>Job Location</li>
<li>Training Period</li>
<li>CTC during & after Training</li>
<li>Designation before & after Training</li>
<li>Service agreement if any - No.of years & Bond amount if any</li>
<li>Degrees & Branches required</li>
<li>Eligibility Criteria - Marks ( X / XII/dip / Current degree/UG for PG) and Arrear Status</li>
<li>Selection Process- Test (Online / Not)/GD/Interview</li>
<li>Facilities Required for campus recruitment</li>
<li>Preferred dates to visit</li></ul>
NB: Placement with Paid Training by students and Consulting fee attached placements are not allowed.
<li>Based on the above data, students will register at Placement Centre. No.of interested students will be informed to the recruiting company.</li>
<li>Based on the no.of interested students, company can fix the venue either at college or at their office. Resumes of the interested students can also be sent to the company for shortlisting at their end. If the policy of the company is to conduct a pooled campus drive for colleges, we are ready to conduct here in our campus.</li>
<li>List of Selected candidates and offer letters shall be given to the Placement Officer at the end of the process. If there is any delay in announcement of results, students will be permitted to attend the next company . If the students get placement in the next company, they will not be permitted to get the offer from the previous company if get selected.</li>
<li>The recruiters are requested to give the feedback of the quality of the students at the end of the selection process and also after the training period. This will help us to improve continuously and offer better numbers than the previous year.
Dream offer will be given to students</li>
<li>To the companies that offer a CTC of more than Rs.4.50 lakhs p.a</li>
<li>To Engineering Companies so that Engineering companies do not fall in short of quality students and also students are given the option to choose their career.</li>
<li>If the student gets placed in the Dream Offer , his previous offer will be cancelled by informing the previous recruiter. This is done to make sure that the student join in only one company. Students placed in the dream offer will not be permitted to attend subsequent Dream offer recruitments.</li>
</ul>

