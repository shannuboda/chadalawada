                        <h1 class="page-title"> Department of Aeronautical Engineering</h1>
                        <strong>Programmes Offered</strong>
                        <br>UG - B.E. Aeronautical Engineering
                        <br>PG - M.E. Avionics<br><br>

                        To provide excellent graduate education in Aeronautical Engineering and continuously support the
                        community of aerospace professionals that will spearhead and strengthen the design and
                        development of aerospace related industries and institutions in India.
                        <br>
                        <div class="page-img"><img src="image/dept/Aeronautical-Engineering.jpg"
                                alt="Aeronautical Engineering" /></div>
                        <p>
                            The department of Aeronautical Engineering was established in the year 2005, offers 4 year
                            B. E programme with an intake of 60. The Department cherishes the hope that its graduates
                            will be the leaders of tomorrow. The goal of the B. E. program in Aeronautical Engineering
                            is to train the students in a broad based manner with equal focus on applications in
                            aircraft engineering, rocket and space technology. The curriculum is designed to impart
                            engineering knowledge in topics such as structural mechanics, aerodynamics, propulsion, and
                            space dynamics. Further provision exists to acquire additional engineering knowledge through
                            electives. Besides, the students acquire sufficient knowledge in mathematics, physics and
                            chemistry under the category of basic sciences and in humanities and engineering arts. The
                            importance of computational-skill enhancement through the development of one’s own programs
                            as well as the use of advanced software is stressed throughout.
                            <br><br>
                            The department is equipped with the best infrastructure and state of the art modern high
                            tech laboratory facilities such as; Aircraft Propulsion Lab., Aerodynamics Lab. (Low Speed
                            Wind Tunnel Facility), Aircraft Structures Lab., Aircraft Simulation Lab. and Design,
                            Modelling and Analysis Lab. (equipped with licensed software viz ANSYS and Catia) catering
                            to B.E. Aeronautical Engineering curriculum as well as to undertake Research activities. The
                            laboratories and workshops, detailed in the curriculum, have been framed to take up the
                            challenges in Industry and focus themselves on research and development.
                            <br><br>
                            Graduates in Aeronautical Engineering can seek employment in civil aviation, defence R&D
                            laboratories, space research organizations, and CSIR laboratories. Aerospace graduates are
                            also preferred by software industries engaged in engineering-software developments. In
                            addition to these, graduates can enter general fields like management and civil services.
                            Those having a flair for higher education and research can pursue M. Tech. and Ph.D. Tie-ups
                            with the pioneering organisation like National Aerospace Laboratories (NAL), Indian Space
                            Research Organizations (ISRO) etc., have served as eye-openers on the complexities of real
                            life problems, both for the students and faculty members. A dedicated project work is taken
                            up by every student for two semesters during final year. The project work is aimed at high
                            quality standards used in current industry practices. Some Project works are also supported
                            by leading MNCs and defence laboratories involved in state of art research and development.
                            <br><br>
                            <strong>VISION</strong>
                            <br>
                            To provide excellent graduate education in Aeronautical Engineering and continuously support
                            the community of aerospace professionals that will spearhead and strengthen the design and
                            development of aerospace related industries and institutions in India.
                            <br><br>
                            <strong>MISSION</strong>
                            <br>
                        <ul>
                            <li>To impart quality exposure in theory and practical with proficiency, skill and humane
                                values with the best of teaching and industrial expertise.</li>
                            <li>To continuously strengthen the laboratory learning of students in tune with the best
                                industry processes and practices</li>
                            <li>To ensure the updated knowledge and skill sets of students in emerging technologies</li>
                            <li>To provide the students the right ambience and opportunities to develop into creative,
                                talented and globally competent Aero professionals.</li>
                            <li>To promote research and development activities in the sphere of aeronautics for the
                                benefit of the society</li>
                        </ul>
                        </p>

