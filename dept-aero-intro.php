          The department of Aeronautical Engineering was established in the year 2005, offers 4 year B. E programme with an intake of 60. The Department cherishes the hope that its graduates will be the leaders of tomorrow. The goal of the B. E. program in Aeronautical Engineering is to train the students in a broad based manner with equal focus on applications in aircraft engineering, rocket and space technology. The curriculum is designed to impart engineering knowledge in topics such as structural mechanics, aerodynamics, propulsion, and space dynamics. Further provision exists to acquire additional engineering knowledge through electives. Besides, the students acquire sufficient knowledge in mathematics, physics and chemistry under the category of basic sciences and in humanities and engineering arts. The importance of computational-skill enhancement through the development of oneâ€™s own programs as well as the use of advanced software is stressed throughout.
