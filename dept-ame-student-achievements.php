					<h4><strong>Mega ATV championship 2024</strong></h4>
                        <p>The electric ATV team EFORZA from the Department of Automobile Engineering, Rajalakshmi Engineering College, Chennai has emerged as the overall champions in the Mega ATV Championship held in Goa from 9th to 12th May 2024, demonstrating excellence in various events through meticulous design and engineering of their ATV's powertrain, steering, suspension, and brake systems. The Mega ATV Championship is an off-road All-Terrain Vehicle competition based on real-life challenges, testing the limits of both vehicles and drivers across diverse and demanding terrains.</p>
<p>In the Flat Dirt Race, the team’s vehicle showcased superior speed and handling over a 9.5 km track, finishing first by clocking the fastest time. The Drag Race saw our ATV accelerating rapidly over 100 meters, securing the runner-up position due to its impressive acceleration and quick time completion.</p>
<p>The Armageddon Race, a combination of the ramp, bridge, tunnel, booby traps, mud, water, rocks, sand, logs and hard jumping off a test of over 350 meters, had our ATV finishing second, thanks to its precise maneuverability and speed. During the Night Endurance Race, our vehicle demonstrated outstanding durability and driver skill by covering the maximum distance within four hours, earning a 2nd runner-up position.</p>
<p>This comprehensive performance, backed by strategic selection and fine-tuning of powertrain, steering, suspension, and brake systems, earned the team a well-deserved overall championship and a cash prize of Rs. 50,000/-. 
</p>
