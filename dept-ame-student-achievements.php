					<h4><strong>Rajalakshmi Engineering College's Automobile Engineering Department Shines Bright at the Luminous SIEP E-Bike Challenge 2024</strong></h4>
					
					<p>
					The Luminous SIEP E-Bike Challenge 2024, held at IES University from January 24 to 28, was a remarkable testament to the engineering ingenuity and commitment to sustainable innovation. Organized by the Imperial Society of Innovative Engineers (ISIEINDIA), this prestigious event attracted 38 teams from across 16 states, showcasing the nation's burgeoning interest in electric vehicle technology.</p>
				<p>	With Luminous Power Technologies as the title sponsor, the competition fostered a diverse exchange of ideas and solutions. Over five days of intense scrutiny, our electric bike prototype underwent rigorous evaluation by a jury of experts hailing from motorsport, automotive, and supplier industries. Criteria ranging from engineering design to marketability were meticulously assessed, ensuring that only the most groundbreaking innovations would prevail.</p>
					<p>The Department of AutomobileEngineering  of our College took immense pride in participating  in this competition for the third time. Our team, aptly named Team Dark, comprised dedicated students and experienced faculty members from the Department. We were already a runner-up  in SIEP2023 last year. Backed by our collective expertise and passion for innovation, we set out to push the boundaries of green mobility.</p>
					<p>Navigating through dynamic tests such as Acceleration, Hill Climb, Self-Balancing and Durability, our team showcased the culmination of months of hard work and dedication. It was a proud moment for us when Team Dark of REC emerged as the Overall Champion, bringing honor not only to our college but also to the state of Tamil Nadu.</p>	
					
					
<img src="image/dept/auto/auto-15.jpg">
					<p>The Flag-Off and Valedictory Ceremonies, graced by industry stalwarts like Luminous Power Technologies, Go Sporty Electric, Jeewan Motors Pvt. Ltd., JBM Group, TATA Motors, Voltme Motors, ABZO Motors, EKA Mobility and academic luminaries from 30+ engineering institutes, served as a platform to celebrate our achievements and forge new collaborations. As representatives of Rajalakshmi Engineering College, we were privileged to be part of such an illustrious gathering, where the future of green mobility was shaped by innovation, determination, and camaraderie.</p>
					
<img src="image/dept/auto/auto-16.jpg">
					
					<p> During our time at college, Team DARK was lauded by the Management, Principal, Heads of Departments, faculty, and fellow students at Rajalakshmi Engineering College. As a token of appreciation, the management has approved a prize of Rs. 1,00,000 for the team
					</p>
