                <h1 class="page-title">Centre of Excellence in Renewable Energy Systems</h1>
                <p>
                    <strong>Vision : </strong>
                    <br>
                    To be a centre, carrying out innovative research work and creating new knowledge enabling the, installation and operation of Renewable Energy systems for various applications, ultimately leading to the large scale replacement of conventional energy installations
                    <br>
                    <strong>Mission : </strong>
				</p>
                <ul>
                    <li>To design highly reliable and cost effective wind energy and solar energy electric conversion ( WEEC and SEEC) systems, build and test prototype models</li>
                    <li>To bring out most appropriate Power Electronic circuits for field operation of WEEC , SEEC and hybrid systems</li>
                    <li>To take up consultancy projects in Renewable Energy systems and enable start-up units in this area.</li>
                    <li>To offer systematic training to academicians and industry personal in the design and operation of Renewable energy systems.</li>
                </ul>
				<p>
                <strong>History of COE-RES in REC: </strong>
                <br>
                This Centre of Excellence in Renewable Energy systems is the long-term dream of the department of Electrical. We concentrated many students project in the field of PV Sourced power generation for commercial, multi-storage and residential buildings in the year 2016. This kind of initiation gives the road map to Renewable Energy Lab in our department. Then the association of many faculties of our department and funded project and relationship with industrial and technical persons we valued now with this outcome. This COE- RES is started in the mid of 2020-2021. Now we are supporting our stake holders in the form of knowledge sharing and initiating research possibilities.
                </p>
				<p>
                <strong>Research Activities: </strong>
