                    <li>Promotes Consultancy projects in renewable energy based multi domain areas</li>
                    <li>Conducts the training programmes, hands on workshops and webinars for both academic and business communities </li>
                    <li>Generates Intellectual Properties (IP) in terms of patents and high-quality technical publications.</li>
                    <li>Promotes the energy audit programme in Industrial, commercial multi storage and residential buildings.</li>
