                <b>1.9 Auto Visual Inspection of 3G box</b> <br>
                This project is used to inspect the 3rd Generation Mobile Switching electronic equipment, manufactured for export purpose. The requirement is to inspect the object for its completeness before it is packaged and dispatched.
                <br>
                <b>Inspection parameters are:</b> <br>
                <h6>Front Side</h6>
                <ul>
                    <li>Presence and absence covers of connectors</li>
                    <li>Presence and alignment of bar code</li>
                </ul>
                <h6>Back side</h6>
                <ul>
                    <li>Presence / absence of fan</li>
                    <li>Presence / absence of Warning Label</li>
                    <li>Presence / absence of Barcodes</li>
                </ul>
                <br>

                <b>Blister inspection system</b> <br>
                This system is capable of inspecting blister packages either packs of capsules or pack of tablets. These packages are being inspected for the following defects.
                <ul>
                    <li>Missing tablets, </li>
                    <li>Broken tablets </li>
                    <li>Colour / shape sequence correctness </li>
                    <li>Label / embossed character verification. </li>
