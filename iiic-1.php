				<h4> Yearly Newsletter </h4>
				<a href="iiic-includes/CONCOR2023.pdf" target="_blank">(Download)</a>
                <p>
                <h4>MAJOR CONSULTANCY PROJECTS UNDERTAKEN BY FACULTY:</h4>
                <b>1.1 IoT - Smart Boat</b> <br>
                IOT - Smart Boat product serves as a platform for all the boat owners to understand the features of the device upon registering through the website and obtain the related products based on their needs. The Main objective of the website is to provide the customers, functional features of our product and gather the customer interest on the product specification by allowing him to register/sign-up in our system. This product makes the smart monitoring for your boat with GPS-position, automatic GeoFence, Triplog, Movement, and your Start Battery. The boat data collected continuously and updated in the related applications both in web and mobile platform. This product serves the need of boat owner in a very small installation steps
                <br>
                <br>

                <b>1.2 Private PACS – A tele radiology software</b> <br>
                A real time PACS – Tele radiology software has been designed and developed to store, exchange, and view medical records through DICOM protocol. The developed PACS is deployed in a cloud and it contains the following:
                <ul>
                    <li>Imaging Modalities Like CT, MRI, Nuclear Medicine, And Ultrasound</li>
                    <li>Storage For Medical Images and Related Data</li>
                    <li>Display Stations For Viewing And Analyzing The Images</li>
                    <li>A Computer Network Connecting The System Components</li>
                </ul>

                <b>1.3 Inspection of Brake Piston Cylinder (BPC) for Automotive Braking System</b> <br>
                BPC is an important component in the brake system of four wheeler. The component is having 16 holes in its periphery. Any small burr left in the BPC can cause serious breakdown of the break system. This project identifies the presence of smallest burr in the hole by rotating the component. The system is a semi-automatic system designed especially based on Brake Piston cylinder inspection requirements.
                <br>
                <br>

                <b>1.4 Inspection of Spline Gear</b> <br>
                This project is again under quality inspection. The designs are unique and it is a customized solution for a leading automobile company. The inspection requirements includes checking of double broach, Groove miss, Model Mix-up and presence and absence of chamfer
                <br>
                <br>

                <b>1.5 Inspection of Flywheel</b> <br>
                Flywheel is an automotive component used in all vehicles. A hot geared wheel is placed on the flywheel and it is pressed by a 70 ton Fly wheel hydraulic press. The direction of the geared wheel is important for different model. Some require with chamfer on the outer side and some do not. Once the hydraulic system presses the ring, the action is irreversible. The wheel is smeared with oil and is also hot. Hence, it is difficult for the operator to judge correctly the side and the company experienced a large wastage of this product due to improper side selection of the component. In this a smart camera is used to detect the side of the ring.
                <br>
                <br>

                <b>1.6 Inspection of Brake Shoe Holder for Heavy Vehicles</b> <br>
                Brake shoe holder of heavy vehicles is forged components that are to be inspected for very small inward and outward bend. The product developed will support in the quality inspection of the 18 models available.
                <br>
                <b>Inspection Parameters are: (Dimension verification for)</b> <br>
                <ul>
                    <li>Inward Bend </li>
                    <li>Outward Bend </li>
                </ul>
                Inner and outer bend of the product is to be measured and compared with the specified values. In case the values are outside limit, the same is to be intimated by alarm as well as by red light in the tower lamp. A trigger is to be passed to automation system to push the NOT OK component into the NOT OK bin.
                <br>
                <br>

                <b>1.7 Inspection of Barcode on Contact Lens Packet </b> <br>
                Cataract lens packets are packaged in a plastic container and details are printed on sheet and pasted on the plastic container. The requirement is to read the details of the cataract lens barcode and print the same details through a printer and paste it on the top cover.
