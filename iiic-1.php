<h1 class="page-title"> Consultancy</h1>
                <!-- <div class="page-img"><img src="image/dept/Automobile-Engineering.jpg" alt="Automobile Engineering" /></div> -->
                <p>
                    The Institute is actively involved not only with local Industry but with Industries having International presence. Consultancy Projects are undertaken with a combination of Faculty & Students of College.
